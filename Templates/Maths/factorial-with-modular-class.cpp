void precompute()
{
    fact[0] = fact[1] = 1;
    for (int i = 2; i < N; i++)
    {
        fact[i] = fact[i - 1] * i;
    }
    invfact[N - 1] = modinv(fact[N - 1]);
    for (int i = N - 2; i >= 0; i--)
    {
        invfact[i] = invfact[i + 1] * (i + 1);
    }
}

Mint nCr(int x, int y)
{
    if (y > x)
        return 0;
    Mint num = fact[x];
    num *= invfact[y];
    num *= invfact[x - y];
    return num;
}


